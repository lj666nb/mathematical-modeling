# 一、数学建模标准流程（5 步法）
# ============================================================

MODELING_PROCESS_5_STEPS = """
## 数学建模五步法

### 第 1 步：问题重述（Problem Restatement）
- 用自己的语言重新描述问题
- 明确已知条件、未知量、约束条件
- 识别问题的核心数学本质（优化？预测？评价？分类？）
- 列出所有变量及其含义

**AI 引导要点**：
- 帮助学生分解复杂问题为子问题
- 识别题目中的关键数据和约束
- 将自然语言问题转化为数学语言

### 第 2 步：模型假设与构建（Model Assumptions & Construction）
- 提出合理的基本假设，简化现实问题
- 根据问题类型选择数学模型
- 定义变量、参数、目标函数、约束条件
- 写出完整的数学表达式

**AI 引导要点**：
- 解释"为什么需要假设"以及"如何检验假设合理性"
- 推荐适合的模型类型（参考 MODEL_CLASSIFICATIONS）
- 帮助学生建立变量之间的数学关系

### 第 3 步：模型求解（Model Solving）
- 选择合适的算法（解析法 / 数值法 / 启发式算法）
- 编写代码实现（Python 为主）
- 进行数值计算，得出结果
- 验证解的合理性

**AI 引导要点**：
- 推荐算法并为学生解释选择理由
- 生成代码框架但让学生理解每行代码
- 帮助调试常见的数值计算错误

### 第 4 步：模型分析（Model Analysis）
- 误差分析：模型预测值与真实值的偏差
- 敏感性分析：参数变化对结果的影响
- 稳定性检验：模型在不同条件下的表现
- 模型对比：与其他方法的优劣比较

**AI 引导要点**：
- 解释每种分析的意义和方法
- 生成分析代码并帮助学生解读结果
- 引导发现模型改进方向

### 第 5 步：模型评价与推广（Evaluation & Extension）
- 总结模型的优点和局限性
- 讨论模型的适用范围
- 提出改进方向
- 考虑模型在其他领域的应用

**AI 引导要点**：
- 帮助学生客观评价自己的模型
- 引导思考"还可以做得更好吗？"
- 联系论文写作要求，转化为论文章节
"""

