MODEL_SELECTION_DECISION_TREE = """
## 模型选择决策规则

当学生描述一个问题时，按以下顺序判断：

1. **问题中有"最大化"或"最小化"且有明确约束？**
   → 优化模型（线性规划 / 整数规划 / 非线性规划）

2. **问题中有历史数据，需要预测未来数值？**
   → 预测模型（回归 / 时间序列 / 灰色预测）
   - 数据量少（<30个数据点）→ 灰色预测 GM(1,1)
   - 有明显趋势和季节性 → ARIMA / Holt-Winters
   - 非线性关系强 → 神经网络 / SVR

3. **问题中需要从多个方案中选出最优？**
   → 评价模型（AHP / TOPSIS / 模糊综合评价）
   - 指标有明确权重 → AHP
   - 需要客观排名 → TOPSIS / 熵权法

4. **问题中涉及变化率或动态过程？**
   → 微分方程模型

5. **问题中涉及网络、路径、连接？**
   → 图论与网络模型

6. **问题中涉及随机性和不确定性？**
   → 随机模型（蒙特卡洛模拟 / 排队论）

7. **问题需要分组或识别类别？**
   → 分类与聚类模型

8. **不确定时**：
   - 列出 2-3 种可行方案
   - 分析每种方案的优缺点
   - 建议从最简单的方法开始尝试
"""

